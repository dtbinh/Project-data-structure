
public class Vertex {
	private int id;
	private String name;
	
	public Vertex(int _id, String _name) {
		this.id = _id;
		this.name = _name;
	}

	public int getId() {
		return id;
	}

	public void setId(int _id) {
		id = _id;
	}

	public String getName() {
		return name;
	}

	public void setName(String _name) {
		name = _name;
	}
}
